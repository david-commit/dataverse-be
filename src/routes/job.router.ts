// =================================================================
//        Router Config
// =================================================================
import express from 'express';
const router = express.Router();

// =================================================================
//        IMPORT: Job db services
// =================================================================
import {
  getAllJobs,
  getJob,
  createJob,
  updateJob,
  deleteJob,
} from '../controllers/jobController';

// =================================================================
//        GET: All Jobs
// =================================================================
router.get('/get-jobs', getAllJobs);

// =================================================================
//        GET: Single Job
// =================================================================
router.get('/get-jobs/:jobID', getJob);

// =================================================================
//        POST: New Jobs
// =================================================================
router.post('/create-job', createJob);

// =================================================================
//        UPDATE: Existing Job
// =================================================================
router.put('/create-job/:jobID', updateJob);

// =================================================================
//        Delete: Existing Job
// =================================================================
router.delete('/delete-job/:jobID', deleteJob);

module.exports = router;
