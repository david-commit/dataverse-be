// =================================================================
//        Router Config
// =================================================================
import express from 'express';
const router = express.Router();

// =================================================================
//        IMPORT: jobs db services
// =================================================================
import {
  getAllJobs,
  getJob,
  createJob,
  updateJob,
} from '../controllers/jobController';

// =================================================================
//        GET: All Jobs
// =================================================================
router.get('/get-jobs', getAllJobs);

// =================================================================
//        GET: Single Job
// =================================================================
router.get('/get-job/:jobID', getJob);

// =================================================================
//        POST: New Jobs
// =================================================================
router.post('/create-job', createJob);

// =================================================================
//        UPDATE: Existing Job
// =================================================================
router.put('/create-job', updateJob);

module.exports = router;
